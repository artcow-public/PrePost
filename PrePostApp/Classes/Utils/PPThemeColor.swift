import UIKit

class PPThemeColor: UIColor {

    static func defaultThemeColor() -> PPThemeColor {
        return PPThemeColor(red: 0x0f / 255.0, green: 0xba / 255.0, blue: 0xa8 / 255.0, alpha: 1)
    }
}
